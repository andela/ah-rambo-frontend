// Article Read Time Addition
export const newArticleList = Articles.map((article) => {
  article.readTime = Math.ceil(article.articleBody.length / 200);
  return article;
});

export const MostUsedCategories = newArticleList.reduce((accumulator, article, index) => {
  const { name } = article.Category ? article.Category : { name: null };
  if (name) {
    const categoryIndex = accumulator.findIndex((category) => Object.keys(category)[0] === name);
    if (categoryIndex >= 0 && index > 0) {
      accumulator[categoryIndex][article.Category.name] += 1;
      return accumulator;
    }
    accumulator.push({ [name]: 1 });
  }
  return accumulator;
}, []);

export const TopCategoriesList = MostUsedCategories.sort((firstCategory, secondCategory) => Object.values(secondCategory)[0] - Object.values(firstCategory)[0]).map((category) => Object.keys(category)[0]);

const CategoryPositions = categories.filter((category) => {
  const positionIndex = TopCategoriesList.findIndex((categoryName) => categoryName === category.name);
  if (positionIndex < 0) return;
  category.position = positionIndex + 1;
  category.link = `/category/${category.name.replace(/[^a-z]/gi, '')}`;
  return category;
});

export const TopCategories = CategoryPositions.sort((firstCategory, secondCategory) => firstCategory.position - secondCategory.position);

export const MostLikedArticles = newArticleList.sort((firstArticle, secondArticle) => secondArticle.likesCount - firstArticle.likesCount);

